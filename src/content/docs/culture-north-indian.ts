  body: `# Dal-roti — the food that means home

In North India the phrase for getting by, for an honest day's bread, for the
plainest contentment, is **dal-roti** — lentils and flatbread. It is a meal and a
metaphor at once. To say a family "earns its dal-roti" is to say it is fed and
dignified, no more and no less. That is the whole genius of *ghar ka khana*, the
home cooking of the North: a handful of spices, a pot of lentils, and a round of
bread off the *tawa* can feed a family richly, night after night, on almost
nothing.

## The thali — a balanced plate by design

The everyday plate is the **thali**: a little of everything, arranged so the cheap
things add up to a complete meal. A scoop of **dal** for protein, **rice** or
**roti** (whole wheat, ground fresh) for the staple energy, a seasonal **sabzi**
(vegetable), a spoon of **yogurt**, a smear of pickle for brightness. Grain and
legume eaten together — *dal-chawal*, *rajma-chawal*, *chana* with roti — do
quietly what corn and beans do across the world: between them the lentil and the
wheat supply the amino acids the other lacks, and a body is properly fed for
pennies. North India worked this out long before anyone drew the protein chart.

## The bhuna masala and the magic of tadka

Underneath nearly every dish is the same patient base: onion, tomato, ginger, and
garlic cooked down slowly in a little oil or **ghee** with **cumin**, **turmeric**,
and chili — the **bhuna masala**. Browned properly, this is the difference between
a dull pot and a deep one. And the technique that makes humble lentils truly sing
is the **tadka**, or **chhonk** — a final flourish where whole spices, cumin
seeds, dried chili, a slice of garlic are bloomed in hot ghee for a few seconds
and poured, crackling, over the cooked dal. The smell of a chhonk hitting the pot
*is* the smell of a North Indian kitchen at dinnertime. It costs a spoonful of fat
and thirty seconds and transforms everything.

## Vegetarian by default, dairy as the workhorse

Much of North India eats little or no meat, and frugality and tradition point the
same way: the protein comes from **legumes** — *dal*, *rajma*, *chana* — and from
**dairy**. Yogurt, ghee, and above all **paneer**, the fresh cheese pressed at
home from curdled milk, are how a vegetarian table stays rich. Paneer is the
affordable "luxury" — *kadhai paneer* or *palak paneer* on the days you want the
meal to feel like a small feast, made from milk that was about to turn anyway.

## Seasons, one-pots, and nothing wasted

The North cooks with the calendar. Winter brings **sarson ka saag** — mustard
greens stewed slow and finished with ghee — eaten with **makki di roti**, the
cornmeal flatbread, a Punjabi pairing as iconic as any in the world. When a body
needs gentling, there is **khichdi**, the soft one-pot of rice and lentils
simmered together that is comfort, sickbed food, and thrift all in one bowl.

And nothing is thrown away. Yesterday's thick dal becomes the filling for
**parathas** at breakfast; stale roti is crumbled into spiced *churma*; vegetable
peels and stems go into the pot or the pickle jar. The philosophy is the one this
challenge is built on: lentils, a few spices, and a flatbread, treated with care,
are not deprivation. They are dinner — and they are home.`,
